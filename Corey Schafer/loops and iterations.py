# For Loop and While Loop
nums = [1,2,3,4,5,56,78]
print()
print('========For Loop=========')
for num in nums:
    print(num)
print()
print('========For Loop - Break Statement=========')
for item in nums:
    if item == 3:
        print('Found It!')
        break
    print(item)
print()

print('========For Loop - Continue Statement =========')
for item in nums:
    if item == 3:
        print('Found It!')
        continue
    print(item)
print()




