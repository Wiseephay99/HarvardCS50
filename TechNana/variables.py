print(f'20 days are {20 * calculate_to_units} {name_of_units}')
print(f'35 days are {35 * calculate_to_units} {name_of_units}')
print(f'50 days are {50 * calculate_to_units} {name_of_units}')
print(f'110 days are {110 * calculate_to_units} {name_of_units}')
print()

